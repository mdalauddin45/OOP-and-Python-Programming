mark=int(input())
if mark>=90:
    print("Your Grade is A+")
elif mark>=80:
    print("Your Grade is A")
elif mark>=70:
    print("Your Grade is B+")
elif mark>=60:
    print("Your Grade is B")
elif mark>=50:
    print("Your Grade is C")
elif mark>=33 :
    print("Your Grade is D")
elif mark<=32:
    print("Your Grade is F")
else:
    print("Invalid input")